// Mengekspor dua fungsi dari modul ini

// Fungsi untuk menyapa dengan nama yang diberikan
export function sayHello(name) {
   // Menampilkan pesan ke konsol dengan nama yang diberikan
   console.info(`Hello ${name}`); // Menampilkan "Hello" diikuti dengan nama yang diteruskan ke fungsi
}

// Fungsi untuk mengucapkan selamat tinggal dengan nama yang diberikan
export function sayGoodBy(name) {
   // Menampilkan pesan ke konsol dengan nama yang diberikan
   console.info(`Goodby ${name}`); // Menampilkan "Goodby" diikuti dengan nama yang diteruskan ke fungsi
}
